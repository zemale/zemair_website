export default function FAQ() {
  const [openIndex, setOpenIndex] = useState<string | null>(null);
  const [searchTerm, setSearchTerm] = useState("");

  const toggleItem = (id: string) => {
    setOpenIndex(openIndex === id ? null : id);
  };

  const filteredFaq = faqData.map(category => ({
    ...category,
    questions: category.questions.filter(q => 
      q.q.toLowerCase().includes(searchTerm.toLowerCase()) || 
      q.a.toLowerCase().includes(searchTerm.toLowerCase())
    )
  })).filter(category => category.questions.length > 0);

  return (
    <div className="pt-32 pb-24 min-h-screen px-6">
      <div className="max-w-4xl mx-auto">
        <h1 className="font-display text-5xl md:text-7xl mb-8 tracking-tight">FAQ</h1>
        <p className="text-zinc-500 text-xl mb-12 font-light leading-relaxed">
          Ответы на часто задаваемые вопросы о выборе, установке и эксплуатации систем ZEMAIR.
        </p>

        <div className="relative mb-20">
          <Search className="absolute left-6 top-1/2 -translate-y-1/2 text-zinc-500" size={20} />
          <input 
            type="text" 
            placeholder="Поиск по вопросам..." 
            className="w-full bg-[#1f1b13] border border-zinc-800 py-6 pl-16 pr-6 font-sans text-lg focus:outline-none focus:border-[#f2ca50] transition-colors"
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
          />
        </div>

        <div className="space-y-16">
          {filteredFaq.map((section, sIdx) => (
            <div key={sIdx}>
              <h2 className="font-display text-xs uppercase tracking-[0.4em] text-[#f2ca50] mb-8 border-b border-zinc-900 pb-4">
                {section.category}
              </h2>
              <div className="divide-y divide-zinc-900">
                {section.questions.map((item, qIdx) => {
                  const id = `${sIdx}-${qIdx}`;
                  const isOpen = openIndex === id;
                  return (
                    <div key={qIdx} className="py-6">
                      <button 
                        onClick={() => toggleItem(id)}
                        className="w-full flex justify-between items-center text-left group"
                      >
                        <span className={`font-display text-xl transition-colors ${isOpen ? 'text-[#f2ca50]' : 'hover:text-[#f2ca50]'}`}>
                          {item.q}
                        </span>
                        {isOpen ? <ChevronUp className="text-[#f2ca50]" /> : <ChevronDown className="text-zinc-700 group-hover:text-zinc-400" />}
                      </button>
                      <AnimatePresence>
                        {isOpen && (
                          <motion.div
                            initial={{ height: 0, opacity: 0 }}
                            animate={{ height: "auto", opacity: 1 }}
                            exit={{ height: 0, opacity: 0 }}
                            transition={{ duration: 0.3 }}
                            className="overflow-hidden"
                          >
                            <p className="pt-6 text-zinc-400 font-light leading-relaxed text-lg">
                              {item.a}
                            </p>
                          </motion.div>
                        )}
                      </AnimatePresence>
                    </div>
                  );
                })}
              </div>
            </div>
          ))}
        </div>

        {filteredFaq.length === 0 && (
          <div className="text-center py-20 bg-[#1f1b13] border border-dashed border-zinc-800">
            <p className="text-zinc-500">По вашему запросу ничего не найдено.</p>
          </div>
        )}

        <div className="mt-32 p-12 bg-[#1f1b13] border border-zinc-800/10 text-center">
           <h3 className="font-display text-2xl mb-4">Не нашли ответ?</h3>
           <p className="text-zinc-400 mb-8 max-w-xl mx-auto">Наша служба поддержки работает ежедневно и готова помочь с любым техническим вопросом.</p>
           <div className="flex flex-col md:flex-row gap-6 justify-center">
              <span className="text-[#f2ca50] font-display text-xl">air@zemair.com</span>
              <span className="text-zinc-700 hidden md:inline">|</span>
              <span className="text-[#f2ca50] font-display text-xl">+7 985 991 63 44</span>
           </div>
        </div>
      </div>
    </div>
  );
}
